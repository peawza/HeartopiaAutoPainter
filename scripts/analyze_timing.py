    print(f"Generation Time: {stats['generation_time']:.3f}s")
    print()
    print("Delay Statistics (seconds):")
    print(f"  Min:       {stats['min']:.4f}s")
    print(f"  5th %ile:  {stats['p5']:.4f}s")
    print(f"  25th %ile: {stats['p25']:.4f}s")
    print(f"  Median:    {stats['median']:.4f}s")
    print(f"  Mean:      {stats['mean']:.4f}s")
    print(f"  75th %ile: {stats['p75']:.4f}s")
    print(f"  95th %ile: {stats['p95']:.4f}s")
